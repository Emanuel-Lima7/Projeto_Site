              <h3 class="text-center mt-5">Hugh Jackman está de volta como Wolverine na nova imagem OFICIAL de Deadpool 3</h3>
              <hr>
              <p class="text-start fs-5">Através de seu Instagram oficial, o astro Ryan Reynolds divulgou uma nova imagem oficial de bastidores de Deadpool 3.
                A foto em questão traz o ator caracterizado como o personagem titular e Hugh Jackman de volta como Wolverine  com seu uniforme clássico dos quadrinhos da Marvel.
                <br>
                Segundo o The Hollywood Reporter, a atriz Jennifer Garner está pronta para reprisar o papel de Elektra no projeto, marcando seu retorno como a personagem depois de vinte anos.
                A atriz interpretou Elektra Natchios pela primeira vez no filme Demolidor, lançado em 2003 pela extinta 20th Century Fox.
                Embora o filme não tenha sido bem recebido pela crítica e pelo público, ele gerou um derivado focado na anti-heroína dois anos depois.
                <br>
                Além disso, há especulações de que Ben Affleck, que interpretou o Demolidor no mesmo filme, também possa fazer uma participação especial no filme. O ator foi supostamente visto no set de filmagens recentemente.
                Embora os detalhes da trama de Deadpool 3 sejam mantidos em segredo, rumores sugerem que o Mercenário Tagarela, interpretado por Ryan Reynolds, causará problemas nas linhas temporais e terá que lidar com a “vigilância” da Autoridade de Variação Temporal (AVT).
                <br>
                Além de Jackman reprisando o papel como Wolverine, existem rumores de que Halle Berry, James Marsden e Famke Janssen podem fazer participações especiais no longa. O trio interpretou os icônicos personagens Tempestade, Ciclope e Jean Grey, respectivamente, na franquia original dos X-Men.
                Vale lembrar que a Marvel Studios antecipou Deadpool 3 para 3 de maio de 2024. Anteriormente, o longa estava previsto para 8 de novembro de 2024.
                O longa está sendo rodado mesmo diante da greve de roteiristas de Hollywood, que vem afetando diversas produções de TV e cinema.
              </p>
