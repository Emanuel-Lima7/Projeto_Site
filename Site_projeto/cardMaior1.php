                e que trouxe novidades de vários títulos que chegam nos próximos meses. Tekken 8 está em produção para PlayStation 5 (PS5), Xbox Series X, Xbox Series S e PC.
                <br>
                <br>
                Os jogadores podem esperar um elenco inicial com 32 lutadores, incluindo nomes já conhecidos como Saheen, Yoshimitsu, Dragunov, Kuma, Steve Fox e Leo. O trailer 
                também traz cenas do modo de história cinematográfico, que deve seguir a fórmula do game anterior e mostrar o estado atual dos personagens no enredo da série, 
                com foco no conflito entre Kazuya e Jin.
                <br>
                <br>
                A novidade mais curiosa, no entanto, foi a modalidade Arcade Quest. Embora seja semelhante a um hub social, ela é descrita como uma experiência single-player 
                em que os jogadores podem criar um avatar e navegar por uma casa de jogos repleta de atividades.
